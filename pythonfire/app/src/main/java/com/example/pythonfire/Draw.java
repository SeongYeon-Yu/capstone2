package com.example.pythonfire;

public class Draw {
    private String profile;

    private String id;
    private String pw;
    private String linkk;

    public Draw() {}

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }

    public String getLinkk() { return linkk; }

    public void setLinkk(String linkk) { this.linkk = linkk; }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPw() {
        return pw;
    }

    public void setPw(String pw) {
        this.pw = pw;
    }


}
